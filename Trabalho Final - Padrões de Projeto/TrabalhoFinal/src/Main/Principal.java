    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
    // 1) Ler valores da interface 
        String nome = txtNome.getText().trim();
        String entrada = txtEntrada.getText().trim();
        String saida = txtSaida.getText().trim();
        String hospedesTexto = txtHospedes.getText().trim();
        int numeroHospedes = Integer.parseInt(hospedesTexto); 
        String tipoQuartoSelecionado = cbQuarto.getSelectedItem().toString();
        String tipoServicoSelecionado = cbServico.getSelectedItem().toString();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha o nome do hóspede.");
            return;
        }
        if (entrada.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, informe a data de entrada.");
            return;
        }
        if (saida.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, informe a data de saida.");
            return;
        }

    //2) Factory Method: cria o Quarto conforme seleção
        //define o tipo da variavel (interface FabricaQuarto
        //fabricaQuarto é a variavel
        FabricaQuarto fabricaQuarto;
        if (tipoQuartoSelecionado.equalsIgnoreCase("Simples")) {
            fabricaQuarto = new FabricaQuartoSimples();
        } else {
            fabricaQuarto = new FabricaQuartoLuxo();
        }
        Quarto quarto = fabricaQuarto.criarQuarto(); // Factory Method cria o objeto Quarto

    //3) Abstract Factory: cria a lista de serviços do pacote
        FabricaServicos pacoteFactory;
        if (tipoServicoSelecionado.equalsIgnoreCase("Econômico")) {
            pacoteFactory = new FabricaServicoEconomico();
        } else {
            pacoteFactory = new FabricaServicoPremium();
        }
        List<Servico> servicosDoPacote = pacoteFactory.criarServicos(); // família coerente de serviços

    //4) Builder: monta a Reserva passo-a-passo
        ReservaBuilder builder = new ReservaBuilder()
                .setNomeHospede(nome)
                .setDataEntrada(entrada)
                .setDataSaida(saida)
                .setNumeroHospedes(numeroHospedes)
                .setQuarto(quarto)
                .setServicos(servicosDoPacote);
        
        Reserva reserva = builder.build(); // reserva pronta (objeto)

    //5) Singleton: adiciona a reserva na lista central do sistema
        GerenciadorReservas.getInstancia().adicionarReserva(reserva);

    //6) Atualiza tabelaReserva: mostrar apenas a reserva criada 
        DefaultTableModel modelo = (DefaultTableModel) tabelaReserva.getModel();
        modelo.setRowCount(0); // limpa a tabela
        adicionarLinhaTabela(modelo, reserva);

        // Feedback simples para o usuário
        JOptionPane.showMessageDialog(this, "Reserva criada com sucesso!");
    }//GEN-LAST:event_btnReservarActionPerformed

    private void btnClonarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClonarActionPerformed
        // Pega a última reserva criada
        Reserva ultima = GerenciadorReservas.getInstancia().getUltimaReserva();
        if (ultima == null) {
            JOptionPane.showMessageDialog(this, "Não há reserva para clonar.");
            return;
        }

        // Prototype: clona a reserva
        Reserva clone = ultima.clonar();

        // adiciona a cópia no gerenciador
        GerenciadorReservas.getInstancia().adicionarReserva(clone);

        // mostra a cópia na tabelaClone (apenas a clonagem)
        DefaultTableModel modelo = (DefaultTableModel) tabelaReserva.getModel();
        modelo.setRowCount(0); // limpa tabela de clone
        adicionarLinhaTabela(modelo, clone);

        JOptionPane.showMessageDialog(this, "Reserva clonada com sucesso!");
    }//GEN-LAST:event_btnClonarActionPerformed

    private void btnTodasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTodasActionPerformed
        List<Reserva> todas = GerenciadorReservas.getInstancia().getTodasReservas();
        DefaultTableModel modelo = (DefaultTableModel) tabelaReserva.getModel();
        modelo.setRowCount(0); // limpa
        for (Reserva r : todas) {
            adicionarLinhaTabela(modelo, r);
        }
        JOptionPane.showMessageDialog(this, "Listadas " + todas.size() + " reservas.");
    }//GEN-LAST:event_btnTodasActionPerformed

