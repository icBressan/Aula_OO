    private void adicionarLinhaTabela(DefaultTableModel modelo, Reserva reserva) {
        modelo.addRow(new Object[]{
            reserva.getNomeHospede(),
            reserva.getNumeroHospedes(),
            reserva.getDataEntrada(),
            reserva.getDataSaida(),
            reserva.getQuarto().getTipo(),
            reserva.getDescricaoServicos()
        });    
    }
